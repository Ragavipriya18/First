public class Ternary {
    public static void main(String[] args) {

        boolean rain = false;
        String res = rain ? "Take an Umbrella" : "Enjoy the sunlight";
        System.out.println(res);

        int num1 = 10, num2 = 40;
        int result = num1 > num2 ? num1 : num2;
        System.out.println(result);
    }
}
